/**
 * Markdown reporter — writes docs/eval-results.md.
 */

import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import type { RunSummary } from './types.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '../../../..');
const OUTPUT_PATH = resolve(REPO_ROOT, 'docs/eval-results.md');

export async function writeReport(summary: RunSummary): Promise<string> {
  const md = renderMarkdown(summary);
  await mkdir(dirname(OUTPUT_PATH), { recursive: true });
  await writeFile(OUTPUT_PATH, md, 'utf-8');
  return OUTPUT_PATH;
}

export function renderMarkdown(summary: RunSummary): string {
  const lines: string[] = [];
  lines.push(`# Eval Results`);
  lines.push('');
  lines.push(`> Generated by \`pnpm eval\` — do not edit by hand.`);
  lines.push('');
  lines.push(`- **Run started:** ${summary.startedAt.toISOString()}`);
  lines.push(`- **Run duration:** ${(summary.totalDurationMs / 1000).toFixed(1)} s`);
  lines.push(`- **Overall pass rate:** ${(summary.overallPassRate * 100).toFixed(1)} %`);
  lines.push(`- **Total estimated cost:** $${summary.totalCostUsd.toFixed(4)}`);
  lines.push('');

  // ---------- Per-agent table ----------
  lines.push(`## Summary`);
  lines.push('');
  lines.push(`| Agent | Model | Cases | Passed | Failed | Pass rate | Avg latency | Cost |`);
  lines.push(`| --- | --- | ---: | ---: | ---: | ---: | ---: | ---: |`);
  for (const a of summary.perAgent) {
    const avgLatency = a.total === 0 ? 0 : Math.round(a.totalLatencyMs / a.total);
    lines.push(
      `| ${a.agent} | \`${a.model}\` | ${a.total} | ${a.passed} | ${a.failed} | ${(a.passRate * 100).toFixed(1)} % | ${avgLatency} ms | $${a.totalCostUsd.toFixed(4)} |`,
    );
  }
  lines.push('');

  // ---------- Per-case detail ----------
  for (const a of summary.perAgent) {
    if (a.cases.length === 0) continue;
    lines.push(`### ${a.agent}`);
    lines.push('');
    lines.push(`| Case | Verdict | Latency | Tokens | Cost | Note |`);
    lines.push(`| --- | :-: | ---: | ---: | ---: | --- |`);
    for (const c of a.cases) {
      const verdict = c.passed ? '✅' : '❌';
      const tokens = `${c.tokensIn}/${c.tokensOut}`;
      const note = c.errorMessage
        ? `error: ${truncate(c.errorMessage, 60)}`
        : truncate(c.reason ?? '', 60);
      lines.push(
        `| ${c.name} | ${verdict} | ${c.latencyMs} ms | ${tokens} | $${c.costUsd.toFixed(4)} | ${note} |`,
      );
    }
    lines.push('');
  }

  return lines.join('\n');
}

function truncate(s: string, max: number): string {
  if (s.length <= max) return s;
  return `${s.slice(0, max - 1)}…`;
}
